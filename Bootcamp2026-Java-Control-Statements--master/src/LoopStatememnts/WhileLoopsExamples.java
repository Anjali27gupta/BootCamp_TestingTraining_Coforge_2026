package LoopStatememnts;

public class WhileLoopsExamples {

	public static void main(String[] args) {
		int count = 0;
		// when condn is true
		while(count<5) {
			System.out.println(count);
			count++;
		}
		// when condn is false
		count =5;
		while(count<5) {
			System.out.println(count);
			count++;
		}

	}

}
